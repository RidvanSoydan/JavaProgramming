public class WhileLoopIntro {


    public static void main(String[] args) {


        if (true){
            System.out.println("Wooden Spoon"); // 1 time
        }

        System.out.println("------------------------------------------------");

        while (true){  // repeated if
            System.out.println("Wooden Spoon"); //infinite times // if the condition true, it will get executed infinite times...
        }

            // this is the just one difference between if and while...
    }
}
