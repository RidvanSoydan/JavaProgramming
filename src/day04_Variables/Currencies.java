package day04_Variables;

public class Currencies {

    public static void main(String[] args) {
        // jpy, pound, peso, cad, riyal...

        int dollars = 1000;

        double lira = dollars * 18.23;
        double euro = dollars * 0.99;
        double ruble = dollars * 60.59;
        double cad = dollars * 1.3;
        double jpy = dollars * 1.42;
        double pound = dollars * 0.86;
        double nok = dollars * 9.93;
        double riyal = dollars * 3.76;
        double chf = dollars * 0.95;
        double kwd = dollars * 0.30;

        System.out.println("dollars = " + dollars);
        System.out.println("kwd = " + kwd);
        System.out.println("chf = " + chf);
        System.out.println("riyal = " + riyal);
        System.out.println("nok = " + nok);
        System.out.println("pound = " + pound);
        System.out.println("jpy = " + jpy);
        System.out.println("cad = " + cad);
        System.out.println("ruble = " + ruble);
        System.out.println("euro = " + euro);
        System.out.println("lira = " + lira);




    }



}
