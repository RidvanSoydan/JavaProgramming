package day09_IfStatements;

public class MinimumNumber_IfElse {

    public static void main(String[] args) {

        //task : Minimum number between two different numbers

        double n1 = -276.21, n2 = -276.12;

        if (n1 > n2) {
            System.out.println("\t" + n2 + " is minimum number");
        } else {
            System.out.println("\t" + n1 + " is minimum number");

        }



    }
}
