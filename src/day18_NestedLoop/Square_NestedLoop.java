package day18_NestedLoop;

public class Square_NestedLoop {

    public static void main(String[] args) {


        for (int i = 0; i < 8; i++) {

            System.out.println();

            for (int j = 0; j < 8; j++) {

                System.out.print( "\t * ");

            }

        }
    }
}
